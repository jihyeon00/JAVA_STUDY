package javabook2.ch02;


public class Boolean {

	public static void main(String[] args) {

		
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);		// b1이 지닌 값 출력
		System.out.println(b2);
		
		int num1 = 10;
		int num2 = 20;
		System.out.println(num1 < num2);
		
		// CPU 조차도 하나의 데이터 타입을 잡아서 응답하고 있음
		// 데이터 타입의 가장 큰 의미는
		// 언어 차원에서 제공하는 데이터 타입 중 하나로 무조건 메모리에 올린다.
	}

}
