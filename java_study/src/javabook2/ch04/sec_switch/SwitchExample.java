package javabook2.ch04.sec_switch;

public class SwitchExample {

	public static void main(String[] args) {
		
		int num;		// 변수 선언
		num = 8 - 5;		// 값 할당
		
		// switch-case 문
		switch(num) {
		
			case 1:
				System.out.println("1");
				break;
			case 2:
				System.out.println("2");
				break;
			case 3:
				System.out.println("3");
				break;
		}
		
	}

}
